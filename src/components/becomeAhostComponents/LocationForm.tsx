interface LocationFormProp {
  city: string;
  setCity: React.Dispatch<React.SetStateAction<string>>;
  street: string;
  setStreet: React.Dispatch<React.SetStateAction<string>>;
  country: string;
  setCountry: React.Dispatch<React.SetStateAction<string>>;
  address: string;
  setAddress: React.Dispatch<React.SetStateAction<string>>;
}

function LocationForm({
  city,
  setCity,
  street,
  setStreet,
  country,
  setCountry,
}: LocationFormProp) {
  const [, setSearchParams] = useSearchParams();
  const [, setCountryCode] = useState("");

  const [, setNewHome] =
    useOutletContext<[Home, React.Dispatch<React.SetStateAction<Home>>]>();

  useEffect(() => {
    if (city !== "" && street !== "" && country !== "") {
      setSearchParams({ step: "selectLocation" });
    } else {
      setSearchParams({ step: "" });
    }

    const selectedCountry = countries.find((c) => c.code === country);

    if (selectedCountry) {
      handleLocationUpdate({
        country: selectedCountry.name, // Set the country name
        countryCode: selectedCountry.code, // Set the country code
      });
    }

    handleLocationUpdate({ address: street });
    handleLocationUpdate({ city: city });
  }, [city, street, country]);

  // Function to handle location updates
  function handleLocationUpdate(
    updatedFields: Partial<Home["loc"] & { hostLocation?: string }>
  ) {
    const localStorageHome = localStorage.getItem("newHome");

    const homeObject: Home = localStorageHome
      ? JSON.parse(localStorageHome)
      : {};

    const updatedHome = {
      ...homeObject,
      loc: {
        ...homeObject.loc, // Preserve existing loc fields
        ...updatedFields, // Update with new fields
      },
      host: {
        ...homeObject.host,
        location: updatedFields.hostLocation || homeObject.host.location, // Update the host location
      },
    };

    setNewHome(updatedHome);
    localStorage.setItem("newHome", JSON.stringify(updatedHome));
  }

  return (
    <div className="">
      <h3 className="text-3xl font-[500] mb-2">Confirm your address</h3>
      <p className="text-lg text-[#9d9d9d] mb-10">
        Your address is only shared with guests after theyâ€™ve made a
        reservation.
      </p>
      <form>
        <div className="mb-4">
          <label className="block text-sm font-medium text-gray-700">
            Country/Region
          </label>
          <Select
            value={country}
            onValueChange={(value) => {
              setCountry(value);
              const selectedCountry = countries.find((c) => c.name === value);
              selectedCountry && setCountryCode(selectedCountry.code);
            }}
          >
            <SelectTrigger className="">
              {country && (
                <span>{countries.find((c) => c.code === country)?.name}</span>
              )}
            </SelectTrigger>
            <SelectContent>
              <SelectGroup>
                {countries.map((country) => (
                  <SelectItem key={country.code} value={country.code}>
                    {country.name}
                  </SelectItem>
                ))}
              </SelectGroup>
            </SelectContent>
          </Select>
        </div>

        <div className="mb-4">
          <label className="block text-sm font-medium text-gray-700">
            Street Address
          </label>
          <Input
            type="text"
            value={street}
            onChange={(e) => setStreet(e.target.value)}
            className="mt-1 block w-full rounded-md border-gray-300 shadow-sm focus:border-black focus:ring-black"
          />
        </div>

        <div className="mb-4">
          <label className="block text-sm font-medium text-gray-700">
            City/Town/Village
          </label>
          <Input
            type="text"
            value={city}
            onChange={(e) => setCity(e.target.value)}
            className="mt-1 block w-full rounded-md border-gray-300 shadow-sm focus:border-black focus:ring-black"
          />
        </div>
      </form>
    </div>
  );
}

export default LocationForm;
